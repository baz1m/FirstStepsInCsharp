// які дозволяють збільшувати або зменшувати значення змінної на одиницю.

int a = 2; a++; // 7.1 Інкремент (++) — збільшує значення змінної на 1.
int b = 2; b--; // 7.2 Декремент (--) — зменшує значення змінної на 1.

