// 4.2 switch - case

// switch — це умовна конструкція, 
// яка дозволяє перевіряти змінну на рівність кільком можливим значенням і 
// виконувати відповідний блок коду для кожного значення. 
// Це альтернатива ланцюжку if-else if, коли потрібно перевірити змінну
// на кілька можливих значень.  

System.Console.WriteLine("Enter number of day");
int day = int.Parse(Console.ReadLine());

switch (day)
{
    case 1:
        Console.WriteLine("Понеділок");
        break;

    case 2:
        Console.WriteLine("Вівторок");
        break;

    case 3:
        Console.WriteLine("Середа");
        break;

    case 4:
        Console.WriteLine("Четвер");
        break;

    case 5:
        Console.WriteLine("П’ятниця");
        break;

    case 6:
        Console.WriteLine("Субота");
        break;

    case 7:
        Console.WriteLine("Неділя");
        break;

    default: //Аналог else  
        Console.WriteLine("Невірний день тижня");
        break;
}

// 5. Цикли 
// Цикли дозволяють виконувати один і той же блок коду кілька разів,
//поки виконується певна умова.

// 5.1 Цикл for
//Цикл for зазвичай використовується,
//коли відома точна кількість повторень.
for (int i = 1; i <= 5; i++)
{
    Console.WriteLine(i);   // Виведе 1, 2, 3, 4, 5
}

//Ініціалізація — виконується один раз на початку
//Умова — перевіряється перед кожною ітерацією.
//Якщо вона істинна, тіло циклу виконується.
//Інкремент/декремент — виконується після кожної ітерації 

// 5.2 Цикл while
int abc = 1;
while (abc <= 5)
{
    Console.WriteLine(abc++);   // Виведе 1, 2, 3, 4, 5
}

// 5.3 Цикл do while
abc = 1;
do
{
    Console.WriteLine(abc);   // Виведе 1, 2, 3, 4, 5
    abc++;
} while (abc <= 5);

// 6. Конвертація даних

// 6.1 Метод Convert
// Методи класу Convert використовуються для
// явної конвертації між різними типами даних.

string numberString = "5"; // Перетворення строки в ціле число
int number = Convert.ToInt32(numberString);

// 6.2 Метод Parse
// Метод Parse перетворює рядок у відповідний тип даних, 
// якщо рядок має правильний формат.
// Якщо формат неправильний, викликається помилка.

numberString = "5"; // Перетворення строки в ціле число
number = int.Parse(numberString);

// 6.3 Метод TryParse
//Метод TryParse схожий на Parse,
// але замість виклику винятку, він повертає true або false залежно від того,
// чи вдалося конвертувати рядок у потрібний тип. Якщо конвертація успішна,
// значення присвоюється змінній, яку ви передали як параметр.

string numberString = "123";
bool success = int.TryParse(numberString, out int number); 
// success буде true, number = 123

string invalidString = "abc";
bool failed = int.TryParse(invalidString, out int invalidNumber);  
// failed буде false, invalidNumber = 0

// 7. Інкремент та декремент у C#

